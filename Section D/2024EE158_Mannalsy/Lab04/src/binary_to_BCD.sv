module binary_to_BCD(  
    input  logic [4:0] A, 
    output logic [7:0] Y 
);   

    always_comb begin
        case (A)

          5'b00000 :Y=8'b00000000; 
          5'b00001 :Y=8'b00000001; 
          5'b00010 :Y=8'b00000010; 
          5'b00011 :Y=8'b00000011; 
          5'b00100 :Y=8'b00000100; 
          5'b00101 :Y=8'b00000101; 
          5'b00110 :Y=8'b00000110; 
          5'b00111 :Y=8'b00000111; 
          5'b01000 :Y=8'b00001000; 
          5'b01001 :Y=8'b00001001; 
          5'b01010 :Y=8'b00010000; 
          5'b01011 :Y=8'b00010001; 
          5'b01100 :Y=8'b00010010; 
          5'b01101 :Y=8'b00010011; 
          5'b01110 :Y=8'b00010100; 
          5'b01111 :Y=8'b00010101; 
          5'b10000 :Y=8'b00010110; 
          5'b10001 :Y=8'b00010111; 
          5'b10010 :Y=8'b00011000; 
          5'b10011 :Y=8'b00011001; 
          5'b10100 :Y=8'b00100000; 
          5'b10101 :Y=8'b00100001; 
          5'b10110 :Y=8'b00100010;   
          5'b10111 :Y=8'b00100011; 
          5'b11000 :Y=8'b00100100; 
          5'b11001 :Y=8'b00100101; 
          5'b11010 :Y=8'b00100110; 
          5'b11011 :Y=8'b00100111; 
          5'b11100 :Y=8'b00101000; 
          5'b11101 :Y=8'b00101001; 
          5'b11110 :Y=8'b00110000; 
          5'b11111 :Y=8'b00110001; 

          default :Y=8'b00000000;
            
        endcase
    end 

endmodule
